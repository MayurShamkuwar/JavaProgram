package OOPs_ClassWork2;

public class AdditionDemo {

	int number1=40;
	int number2=20;
	int result;
	
	void addition()
	{
		result=number1+number2;
		System.out.println("Results: "+result);
	}
	void subtraction()
	{
		result=number1-number2;
		System.out.println("Results: "+result);
	}
	void multiplication()
	{
		result=number1*number2;
		System.out.println("Results: "+result);
	}
	void division()
	{
		result=number1/number2;
		System.out.println("Results: "+result);
	}
}
